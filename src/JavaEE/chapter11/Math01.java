public class Math01 {
    public static void main(String[] args) {
        System.out.println(Math.abs(-1));       //绝对值abs
        System.out.println(Math.max(10,11));    //求最大值max
        System.out.println(Math.min(9,10));     //求最小值min
        System.out.println(Math.floor(5.4));    //向下取整floor
        System.out.println(Math.ceil(5.4));     //向上取整ceil
        System.out.println(Math.pow(2,4));      //求幂，pow
        System.out.println(Math.round(5.5));    //四舍五入round
        System.out.println(Math.random());      //求随机数，范围是0-1

    }
}
